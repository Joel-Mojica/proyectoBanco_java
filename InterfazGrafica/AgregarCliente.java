    public void cargarArticulos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaDeEntrada.getModel();
        modelo.setRowCount(0);
        res = ConeccionBaseDatos.Conexion.Consulta("select * from articulo");
        try {
            while (res.next()) {
                Vector listaTabla = new Vector();
                listaTabla.add(res.getInt(1));
                listaTabla.add(res.getString(2));
                listaTabla.add(res.getString(3));
                listaTabla.add(res.getString(4));
                listaTabla.add(res.getString(5));
                listaTabla.add(res.getInt(6));
                listaTabla.add(res.getInt(7));
                modelo.addRow(listaTabla);
                tablaDeEntrada.setModel(modelo);

            }
        } catch (SQLException e) {

        }
    }

