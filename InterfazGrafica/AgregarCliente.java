    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (txtNombre.getText().isEmpty() || txtPrimerApellido.getText().isEmpty() || txtSegundoApellido.getText().isEmpty() || txtDireccion.getText().isEmpty() || txtEdad.getText().isEmpty() || txtSaldo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los datos solicitados", "informacion", JOptionPane.INFORMATION_MESSAGE);
            txtNombre.setText("");
            txtPrimerApellido.setText("");
            txtSegundoApellido.setText("");
            txtDireccion.setText("");
            txtEdad.setText("");
            txtSaldo.setText("");
        } else {
            try {
                res = ConeccionBaseDatos.Conexion.Consulta("Select COUNT(Nombre) from articulo where nombre = '" + txtNombre.getText() + "'");
                try {
                    while (res.next()) {
                        cont = res.getInt(1);
                    }
                } catch (SQLException ex) {

                }
                if (cont >= 1) {
                    JOptionPane.showMessageDialog(this, "Este elemento ya existe, digite otro", "informacion", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    Procedimientos.entradaArticulos(txtNombre.getText(), txtPrimerApellido.getText(), txtSegundoApellido.getText(), txtDireccion.getText(), Integer.parseInt(txtEdad.getText()), Integer.parseInt(txtNumeroDeCuenta.getText()),Integer.parseInt(txtSaldo.getText()));
                    txtNombre.setText("");
                    txtPrimerApellido.setText("");
                    txtSegundoApellido.setText("");
                    txtDireccion.setText("");
                    txtEdad.setText("");
                    txtSaldo.setText("");
                    txtNumeroDeCuenta.getText();
                    JOptionPane.showMessageDialog(this, "Los Datos han sido guardados correctamente", "informacion", JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (SQLException ex) {

            }
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void opcionEliminarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opcionEliminarDatosActionPerformed
        int row = tablaDeEntrada.getSelectedRow();
        int opc = JOptionPane.showConfirmDialog(this, "Estas seguro que desea eliminar este registro, la fila del registro es la numero: " + (row +1), "pregunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opc == JOptionPane.YES_OPTION) {
            try {
                Procedimientos.eliminarArticulos(Integer.parseInt(tablaDeEntrada.getValueAt(row, 0).toString()));
            } catch (SQLException ex) {

            }
        }


    }//GEN-LAST:event_opcionEliminarDatosActionPerformed

